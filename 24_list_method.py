l1 = [1,8,7,2,21,15]
print(l1)
l1.sort() # sorts the list
print(l1)
l1.reverse() # reverse the list
print(l1)
l1.append(45) # adds at the end of the list
print(l1)
l1.insert(1, 544) # inserts 544 at index 1
print(l1)
l1.pop(2) # removes element at index 2
print(l1)
l1.remove(21) # remove 21 from the list 
print(l1)